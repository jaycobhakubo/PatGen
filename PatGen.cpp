int ProcessMultiList(MULTILIST* ml, BOOL outnum)
{
	int ii,jj,kk;
	int num = 0;
	DWORD req_num[16];
	int rout = 0;



	while (ml->hits != 0)
	{
		int hits = ml->hits;
		BOOL no_overlap = FALSE;
		if (hits < 0)
		{
			hits = -hits;
			no_overlap = TRUE;
		}
		BYTE* bp = ml->mlist;

			int numb = 0;
			while (bp[numb] != END_OF_MASKS) numb++;
			numb = numb/25;

			// build the binary masks
			int indi = 0;
			for(jj=0;jj<numb;jj++,bp+=25,indi++)
			{
				DWORD m = 0;
				DWORD bitf = 1;

				for(kk=0;kk<25;kk++)
				{
					if (bp[kk]) m |= bitf;
					bitf = bitf<<1;
				}
				tmlist[indi] = m;
			}

		if (outnum)
		{
			req_num[rout++] = hits;
		 	for(ii=0;ii<indi;ii++)
		 		masklist[num++] = tmlist[ii];
		}
		else
		{
			if (hits == 1 || hits == -1)
			{ // output as is
				for(ii=0;ii<indi;ii++)
					masklist[num++] = tmlist[ii];
			}

			int org_indi = indi;

			if (hits >= 2)
			{
				// cross with itself into masklist1
				indi = Cross(tmlist,indi,tmlist,indi,masklist1,no_overlap);
			}

			if (hits == 2)
			{
				for(ii=0;ii<indi;ii++)
					masklist[num++] = masklist1[ii];
			}

			if (hits == 3)
			{ // cross tmlist with masklist1 and output
				int final_indi = Cross(tmlist,org_indi,masklist1,indi,masklist2,no_overlap);
				for(ii=0;ii<final_indi;ii++)
					masklist[num++] = masklist2[ii];
			}
		}

		masklist[num++] = MLIST_DELIMITER;

		ml++; // move to next mulitlist specifier
	}

	if (outnum)
	{ // add req_num to end of mask list
		for(ii=0;ii<rout;ii++)
			masklist[num++] = req_num[ii];
	} 

	TRACE("ProcessMultiList: num = %d\n",num);

	return(num);
}


int Cross(DWORD* ml1,int len1,DWORD* ml2, int len2, DWORD* ml3, BOOL nooverlap)
{
	int ii,jj,kk;
//	int nooverlap = FALSE;

				// now cross ml1 and ml2, no complete overlaps
				int indi = 0;
				for(jj=0;jj<len1;jj++)
				{
					for(kk=0;kk<len2;kk++)
					{
						DWORD m1 = ml1[jj];
						DWORD m2 = ml2[kk];
						DWORD cm = m1 | m2;
						DWORD am = m1 & m2;
						if (m1 == m2) continue;
						if (am == m1 || am == m2) continue;
						if (nooverlap && (am != 0)) continue;
						{ // and the composite mask
							// make sure this isn't already in the list
							for(ii=0;ii<indi;ii++)
							{
								if (cm == ml3[ii]) break;
							}

							if (ii >= indi)
								ml3[indi++] = cm;
						} // end if
					} // end for kk
				} // end for jj

//	TRACE("Cross = %d\n",indi);

	return(indi);
}

int ProcessORList(ORLIST* ml)
{
	int ii/*,jj,kk*/;
	int num = 0;
	int tnum,tnum1;

	while (ml->flags >= 0)
	{
		tnum = ByteListtoMaskList(ml->list0,tmlist);

		if (ml->list1 != NULL)
		{
			tnum1 = ByteListtoMaskList(ml->list1,masklist1);
			tnum = Cross(tmlist,tnum,masklist1,tnum1,masklist2,ml->flags);
			// tmlist * masklist1 = masklist2
			if (ml->list2 != NULL)
			{
				tnum1 = ByteListtoMaskList(ml->list2,masklist1);
				tnum = Cross(masklist2,tnum,masklist1,tnum1,tmlist,ml->flags);
				// masklist2 * masklist1 = tmlist
				if (ml->list3 != NULL)
				{
					tnum1 = ByteListtoMaskList(ml->list3,masklist1);
					tnum = Cross(tmlist,tnum,masklist1,tnum1,masklist2,ml->flags);
					// tmlist * masklist1 = masklist2
					for(ii=0;ii<tnum;ii++)
						tmlist[ii] = masklist2[ii];
				}
			}
			else
			{
				for(ii=0;ii<tnum;ii++)
					tmlist[ii] = masklist2[ii];
			}
		}

		// add tmlist with len tnum to output list
		for(ii=0;ii<tnum;ii++) masklist[num++] = tmlist[ii];

		ml++;
	}
	
	return(num);
}

