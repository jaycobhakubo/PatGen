// convert mask list to the BYTE definition
BYTE* masks_to_BYTE(DWORD* ml, int num)
{
	int ii,jj;
	int oo = 0;

	BYTE* bp = (BYTE *)malloc(num*25+1);

	for(ii=0;ii<num;ii++)
	{
		DWORD m = 1;
		DWORD am = ml[ii];
		for(jj=0;jj<25;jj++)
		{
			if (am & m)
				bp[oo]=1;
			else
				bp[oo]=0;
//			printf("%d",bp[oo]);
//			if (jj%5 == 4) printf("\n");
			oo++;
			m = m<<1;
		}
	}

	bp[oo] = 100;

	return(bp);
}

int CopyByteList(BYTE* dst, BYTE* src)
{
	int ii=0;
	while (src[ii] != 100)
		{ dst[ii] = src[ii]; ii++; }
	dst[ii] = src[ii];

	return(ii/25);
}

