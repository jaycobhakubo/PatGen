BOOL ProcessArgs( int   argc,
                  char *argv[] )
{
    for ( int nArg = 1;  nArg < argc;  nArg++ )
    {
        // Get shortcut to paramater text
        char *pArg  =  argv[ nArg ];
        int   nLen  =  strlen( pArg );

        if ( nLen <= 1 )
        {
            // Insufficient data
            return FALSE;
        }

        if ( pArg[ 0 ]  ==  '/'  ||
             pArg[ 0 ]  ==  '-' )
        {
            // Looks like a switch - Examine all switches in this parameter
            for ( int nPos = 1;  nPos < nLen;  nPos++ )
            {
                BOOL bSkipRemaining = FALSE;

                switch ( toupper( pArg[ nPos ] ) )
                {
                    case 'V':

                        bVerboseMode = TRUE;
                        break;


                    case 'R':

                        bShowReport = TRUE;

                        if ( pArg[ nPos + 1 ]  ==  '=' )
                        {
                            // Looks like we have a report filename - parse it
                            if ( pArg[ nPos + 2 ]  ==  '\0' )
                            {
                                // Missing filename
                                return FALSE;
                            }

                            // Got a filename
                            pszReportFilename  =  pArg + nPos + 2;
                            bSkipRemaining     =  TRUE;
                        }

                        break;


                    default:

                        // Invalid switch
                        return FALSE;
                }

                if ( bSkipRemaining )
                {
                    // Go to the next parameter (if any)
                    break;
                }
            }
        }
        else
        {
            // Dunno how to deal with this kind of parameter
            return FALSE;
        }
    }

    // Check if no report filename was provided
    if ( bShowReport  &&
         pszReportFilename == NULL )
    {
        // Give it a default filename
        pszReportFilename = "Report.txt";
    }

    return TRUE;
}


