int ByteListtoMaskList(BYTE* bp, DWORD* mlist)
{
	int num;
	int jj,kk;
	int numb = 0;
	while (bp[numb] != END_OF_MASKS) numb++;
	num = numb/25;

	BOOL mask_group = 0;

	int indi = 0;
	//int len1,len2;
	for(jj=0;jj<num;jj++,bp+=25,indi++)
	{
		DWORD m = 0;
		DWORD bitf = 1;

		if (bp[0] == 'y' || bp[0] == 'Y')
		{
			mask_group = 1;
			bp++;
		}

		for(kk=0;kk<25;kk++)
		{
			if (bp[kk]) m |= bitf;
			bitf = bitf<<1;
		}
		if (mask_group)
		{
			m |= MASKGROUP_BIT;
		}
		mlist[indi] = m;
	}
	return(num);
}

